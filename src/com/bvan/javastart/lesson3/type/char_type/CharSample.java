/**
 * @author bvanchuhov
 */
public class CharSample {

    public static void main(String[] args) {
        char c1 = 'a';
        char c2 = '\\';
        char c3 = '\u2764';
        char c4 = 23124;

        System.out.println("c1 = " + c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
        System.out.println("c4 = " + c4);
    }
}
